#!/usr/bin/env python3
"""
Cross-platform build script for HOI4 Content Maker.

Usage:
    python build/build.py

Works on Windows, macOS, and Linux.
"""

import os
import subprocess
import sys
import shutil

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
ROOT_DIR = os.path.dirname(SCRIPT_DIR)
SOURCE = os.path.join(ROOT_DIR, "hoi4_content_maker.py")


def run(cmd, **kwargs):
    print(f"  $ {' '.join(cmd)}")
    subprocess.check_call(cmd, **kwargs)


def ensure_package(package, import_name=None):
    import_name = import_name or package
    try:
        __import__(import_name)
    except ImportError:
        print(f"  [INFO] Installing {package}...")
        run([sys.executable, "-m", "pip", "install", package])


def generate_icon():
    ico_path = os.path.join(SCRIPT_DIR, "icon.ico")
    png_path = os.path.join(SCRIPT_DIR, "icon.png")

    if sys.platform == "darwin":
        needed = not os.path.exists(png_path)
    elif sys.platform.startswith("linux"):
        needed = not os.path.exists(png_path)
    else:
        needed = not os.path.exists(ico_path)

    if not needed:
        return

    print("  [INFO] Generating icon...")
    ensure_package("Pillow", "PIL")

    gen_script = os.path.join(SCRIPT_DIR, "generate_icon.py")
    run([sys.executable, gen_script, "--all-formats"],
        cwd=SCRIPT_DIR)


def write_spec():
    """Generate a platform-aware PyInstaller .spec file."""
    icon_line = ""
    version_line = ""

    if sys.platform == "win32":
        icon_line = "    icon=os.path.join(SCRIPT_DIR, 'icon.ico'),"
        version_line = "    version=os.path.join(SCRIPT_DIR, 'version_info.txt'),"
    elif sys.platform == "darwin":
        png = os.path.join(SCRIPT_DIR, "icon.png")
        if os.path.exists(png):
            icon_line = f"    icon=os.path.join(SCRIPT_DIR, 'icon.png'),"
    # Linux ELF executables don't support embedded icons — skip

    source_path = SOURCE.replace("\\", "/")
    root_path = ROOT_DIR.replace("\\", "/")
    script_dir_str = SCRIPT_DIR.replace("\\", "/")

    spec_content = f"""# -*- mode: python ; coding: utf-8 -*-
# Auto-generated by build.py — do not edit manually
import os
SCRIPT_DIR = {script_dir_str!r}

block_cipher = None

a = Analysis(
    [{source_path!r}],
    pathex=[{root_path!r}],
    binaries=[],
    datas=[],
    hiddenimports=[
        'hoi4_logger',
        'tkinter', 'tkinter.ttk', 'tkinter.messagebox',
        'tkinter.filedialog', 'tkinter.font', 'tkinter.scrolledtext',
        'PIL', 'PIL.Image', 'PIL.ImageTk',
        'json', 'os', 're', 'threading', 'sys',
        'subprocess', 'tempfile', 'uuid', 'copy', 'hashlib', 'ast',
    ],
    hookspath=[],
    runtime_hooks=[],
    excludes=[
        'matplotlib', 'numpy', 'scipy', 'pandas',
        'PyQt5', 'PyQt6', 'wx', 'unittest',
        'email', 'http', 'xmlrpc', 'lib2to3',
    ],
    cipher=None,
    noarchive=False,
)

pyz = PYZ(a.pure, a.zipped_data, cipher=None)

exe = EXE(
    pyz,
    a.scripts,
    a.binaries,
    a.zipfiles,
    a.datas,
    [],
    name='HOI4ContentMaker',
    debug=False,
    strip=False,
    upx=True,
    upx_exclude=[],
    console=True,
{icon_line}
{version_line}
)
"""
    spec_path = os.path.join(SCRIPT_DIR, "hoi4_content_maker_build.spec")
    with open(spec_path, "w") as f:
        f.write(spec_content)
    return spec_path


def get_output_name():
    if sys.platform == "win32":
        return "HOI4ContentMaker.exe"
    elif sys.platform == "darwin":
        return "HOI4ContentMaker-mac"
    else:
        return "HOI4ContentMaker-linux"


def main():
    output_name = get_output_name()

    print()
    print("=" * 55)
    print("  HOI4 Content Maker | Cross-Platform Build")
    print(f"  Platform: {sys.platform}  |  Python: {sys.version.split()[0]}")
    print("=" * 55)
    print()

    # Check source exists
    if not os.path.exists(SOURCE):
        print(f"  [ERROR] Source not found: {SOURCE}")
        sys.exit(1)

    # Ensure dependencies
    print("[STEP 1/4] Checking dependencies...")
    ensure_package("pyinstaller", "PyInstaller")
    ensure_package("Pillow", "PIL")
    print()

    # Generate icon
    print("[STEP 2/4] Generating icon...")
    generate_icon()
    print()

    # Clean previous build
    print("[STEP 3/4] Cleaning previous build...")
    build_tmp = os.path.join(SCRIPT_DIR, "build_tmp")
    if os.path.exists(build_tmp):
        shutil.rmtree(build_tmp)
    old_exe = os.path.join(ROOT_DIR, output_name)
    if os.path.exists(old_exe):
        os.remove(old_exe)
    print()

    # Build
    print("[STEP 4/4] Compiling... (1-3 minutes)")
    print()

    spec_path = write_spec()

    try:
        run([
            sys.executable, "-m", "PyInstaller",
            spec_path,
            "--clean", "--noconfirm",
            "--distpath", ROOT_DIR,
            "--workpath", build_tmp,
        ])
    except subprocess.CalledProcessError:
        print()
        print("=" * 55)
        print("  [ERROR] Build FAILED — see output above")
        print("=" * 55)
        sys.exit(1)
    finally:
        # Cleanup
        if os.path.exists(build_tmp):
            shutil.rmtree(build_tmp)
        if os.path.exists(spec_path):
            os.remove(spec_path)

    # Rename output on non-Windows platforms
    default_out = os.path.join(ROOT_DIR, "HOI4ContentMaker")
    if sys.platform == "win32":
        default_out += ".exe"
    final_out = os.path.join(ROOT_DIR, output_name)

    if default_out != final_out and os.path.exists(default_out):
        if os.path.exists(final_out):
            os.remove(final_out)
        os.rename(default_out, final_out)

    if os.path.exists(final_out):
        size_mb = os.path.getsize(final_out) / (1024 * 1024)
        print()
        print("=" * 55)
        print(f"  Build SUCCESSFUL")
        print(f"  Output: {output_name}  ({size_mb:.1f} MB)")
        print("=" * 55)
        print()
    else:
        print()
        print("  [ERROR] Build output not found")
        sys.exit(1)


if __name__ == "__main__":
    main()
